    def download_exe(self):
        folder = os.path.dirname(__file__)
        
        uname = platform.uname()
        cpu = uname.machine.lower()
        opsystem = uname.system.lower()
        
        files = {
            'linux-aarch64': 'https://github.com/allixender/dggrid4py/releases/download/v0.3.2/dggrid-linux-aarch-gnu',
            'linux-x86_64': 'https://github.com/allixender/dggrid4py/releases/download/v0.3.2/dggrid-linux-x64',
            'darwin-arm64': 'https://github.com/allixender/dggrid4py/releases/download/v0.3.2/dggrid-macos-aarch',
            'windows-amd64': 'https://github.com/allixender/DGGRID/releases/download/v8.43b-1/dggrid-windows.exe'
        }

        url = files.get(f"{opsystem}-{cpu}", None)
        if url is None:
            raise ValueError(f"No portable executable available for {opsystem} {cpu}. Please use dggrid4py with a local DGGRID installation.")
                        
        filename = os.path.split(url)[-1]
        self.local_path = os.path.join(folder, filename)

        if not os.path.isfile(self.local_path):
            res = QMessageBox.question(None, "Message", """Executable file is not available. Do you want to download?""")
            if res == QMessageBox.Yes:
                try:
                    try:
                        response = requests.get(url, stream=True)
                    except:
                        response = requests.get(url, stream=True, verify=False)
                        
                    response.raise_for_status()
                    
                    with open(self.local_path, 'wb') as f:
                        for chunk in response.iter_content(chunk_size=8192):
                            f.write(chunk)

                    current_permissions = os.stat(self.local_path).st_mode
                    try:
                        os.chmod(self.local_path, current_permissions | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
                    except:
                        pass
                    
                    if os.path.isfile(self.local_path):
                        self.dlg.show()

                except:
                    pass            
            else:
                pass
        else:
            self.dlg.show()
    
    def createShapefile(self, clip_wkts, tmp_uuid, crs_wkt=None):
        outfile_name = f"region_{tmp_uuid}.shp"
        outfile_path = os.path.join(self.dlg.le_wdir.text(), outfile_name)
        
        driver = ogr.GetDriverByName("ESRI Shapefile")
        ds = driver.CreateDataSource(outfile_path)
        srs = osr.SpatialReference()
        if crs_wkt:
            srs.ImportFromWkt(crs_wkt)
        else:
            srs.ImportFromEPSG(4326)
        
        layer = ds.CreateLayer("region", srs, ogr.wkbPolygon)
        defn = layer.GetLayerDefn()
        
        for clip_wkt in clip_wkts:
            ogr_geom = ogr.CreateGeometryFromWkt(clip_wkt)
            
            feat = ogr.Feature(defn)
            feat.SetGeometry(ogr_geom)
        
            layer.CreateFeature(feat)
        
        ds = layer = defn = feat = None        
        
        return outfile_path
        
    
    def createPath1(self, shp_path, dtype):
        extention = {
            "SHP" : "shp",
            "GPKG" : "gpkg",
            "KML" : "kml",
            "GML" : "gml",
            "GEOJSON" : "geojson"
        }
        
        output_type = self.dlg.cb_dtype.currentText()
        
        outfile_name_ = self.dlg.le_outFileName.text()
        
        ext = extention[output_type]
        
        if outfile_name_:
            if dtype == "point":
                outfile_name = "point_" + outfile_name_.removesuffix(f".{ext}").replace(f".{ext}", f"_{ext}") + f".{ext}"
            else:
                outfile_name = "cell_" + outfile_name_.removesuffix(f".{ext}").replace(f".{ext}", f"_{ext}") + f".{ext}"
            
            suffix = 0
            while True:
                outfile_path = os.path.join(self.dlg.le_wdir.text(), outfile_name) if suffix==0 else os.path.join(self.dlg.le_wdir.text(), outfile_name.replace(f".{ext}", f"_{suffix}.{ext}"))    
                if not os.path.isfile(outfile_path):
                    break
                suffix += 1
            
        else:
            outfile_path = shp_path.replace("shp", ext)
        
        return outfile_path
    
