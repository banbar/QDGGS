    def createGPKG(self, shp_path, dtype):
        
        output_type_dict = {
            "SHP" : "ESRI Shapefile",
            "GPKG" : "GPKG",
            "KML" : "KML",
            "GML" : "GML",
            "GEOJSON" : "GeoJSON"
        }
        
        output_type = self.dlg.cb_dtype.currentText()
        
        outfile_path = self.createPath1(shp_path, dtype)
        
        vec = ogr.Open(shp_path, 1)
        layer_shp = vec.GetLayer()
        features = list(layer_shp)
        
        vec = layer_shp = None
        
        for p in glob(os.path.join(shp_path.replace(".shp","") + "*")):
            if p.endswith("dbf") or p.endswith("shp") or p.endswith("shx"):
                os.remove(p)
        
        driver = ogr.GetDriverByName(output_type_dict[output_type])
        ds = driver.CreateDataSource(outfile_path)
        srs = osr.SpatialReference()
        srs.ImportFromEPSG(4326)
        
        if self.dlg.tabWidget.currentIndex() == 0:
            layer_name = f"dggrid_{self.dlg.cb_dggstype.currentText().lower()}_r{self.dlg.sb_resolution.value()}"
            
        elif self.dlg.tabWidget.currentIndex() == 1:
            layer_name = f"dggrid_{self.dlg.cb_dggstype_2.currentText().lower()}_r{self.dlg.sb_resolution_2.value()}"            
                
        if dtype == "polygon":
            layer = ds.CreateLayer(layer_name, srs, ogr.wkbPolygon)
        else:
            layer = ds.CreateLayer(layer_name, srs, ogr.wkbPoint)

        layer.CreateField(ogr.FieldDefn("global_id", ogr.OFTInteger64))
        defn = layer.GetLayerDefn()
        
        for en, feat_ in enumerate(features, 1):
            global_id = feat_.GetField("global_id")
            geom = feat_.GetGeometryRef()
            
            if output_type == "KML": geom.SwapXY()

            minx, maxx, miny, maxy = geom.GetEnvelope()
            diff_x = maxx - minx

            if (diff_x > 300):continue

            feat = ogr.Feature(defn)

            feat.SetGeometry(geom)
            feat.SetField("global_id", global_id)
            
            layer.CreateFeature(feat)
                  
        ds.FlushCache()
        
        ds = layer = defn = feat = None
        
        if self.dlg.cb_add_to_layer.isChecked():
            try:
                layer = QgsVectorLayer(outfile_path, layer_name, "ogr")
                QgsProject.instance().addMapLayer(layer)
            except:
                pass
        
    def createGPKG2(self, hexagons, out_path, out_type="polygon", child_res=None):
        
        output_type_dict = {
            "SHP" : "ESRI Shapefile",
            "GPKG" : "GPKG",
            "KML" : "KML",
            "GML" : "GML",
            "GEOJSON" : "GeoJSON"
        }
        
        tmp_uuid = str(uuid.uuid4())
        output_type = self.dlg.cb_dtype.currentText()
        
        outfile_name_ = self.dlg.le_outFileName.text()
        ext = output_type.lower()
        
        if outfile_name_:
            if out_type == "polygon":
                outfile_name = outfile_name_.removesuffix(f".{ext}").replace(f".{ext}", f"_{ext}") + f".{ext}"
            elif out_type == "neighbor":
                outfile_name = "nbr_" + outfile_name_.removesuffix(f".{ext}").replace(f".{ext}", f"_{ext}") + f".{ext}"
            elif out_type == "children":
                outfile_name = "chd_" + outfile_name_.removesuffix(f".{ext}").replace(f".{ext}", f"_{ext}") + f".{ext}"
        else:
            if out_type == "polygon":
                outfile_name = f"cell_h3_r{self.dlg.sb_resolution.value()}_{tmp_uuid}.{ext}"
            elif out_type == "neighbor":
                outfile_name = f"nbr_h3_r{self.dlg.sb_resolution.value()}_{tmp_uuid}.{ext}"
            elif out_type == "children":
                outfile_name = f"chd_h3_r{child_res}_{tmp_uuid}.{ext}"
        
        outfile_path = os.path.join(out_path, outfile_name)
        
        if os.path.isfile(outfile_path):
            outfile_path = os.path.join(out_path, outfile_name.replace(f".{ext}", f"_{tmp_uuid}.{ext}"))
        
        driver = ogr.GetDriverByName(output_type_dict[output_type])
        ds = driver.CreateDataSource(outfile_path)
        srs = osr.SpatialReference()
        srs.ImportFromEPSG(4326)
        
        if self.dlg.tabWidget.currentIndex() == 0:
            layer_name = f"dggrid_{self.dlg.cb_dggstype.currentText().lower()}_r{self.dlg.sb_resolution.value()}"
            
        elif self.dlg.tabWidget.currentIndex() == 1:
            if child_res:
                layer_name = f"dggrid_{self.dlg.cb_dggstype_2.currentText().lower()}_r{child_res}"
            else:
                layer_name = f"dggrid_{self.dlg.cb_dggstype_2.currentText().lower()}_r{self.dlg.sb_resolution_2.value()}"
                
        layer = ds.CreateLayer(layer_name, srs, ogr.wkbPolygon)

        layer.CreateField(ogr.FieldDefn("global_id", ogr.OFTString))
        defn = layer.GetLayerDefn()
        
        for global_id, geom_shapely in hexagons:
            
            geom_wkt = geom_shapely.wkt
            geom_ogr = ogr.CreateGeometryFromWkt(geom_wkt)
            
            if output_type == "KML": geom_ogr.SwapXY()
            
            feat = ogr.Feature(defn)

            feat.SetGeometry(geom_ogr)
            feat.SetField("global_id", global_id)
            
            layer.CreateFeature(feat)
                  
        ds.FlushCache()
        
        ds = layer = defn = feat = None
        
        if self.dlg.cb_add_to_layer.isChecked():
            try:
                layer = QgsVectorLayer(outfile_path, layer_name, "ogr")
                QgsProject.instance().addMapLayer(layer)
            except:
                pass
        
