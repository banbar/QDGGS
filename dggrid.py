            dgg_type = self.dlg.cb_dggstype.currentText() + "_t1"
            
            if not (self.dlg.chb_polygon.isChecked() or self.dlg.chb_centroid.isChecked() or self.dlg.chb_metafile.isChecked()):
                QMessageBox.critical(None, "ERROR", """Output Type is not selected!""")
                return
        
